    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sql="Select * from NewBook where BookId=?";
        try
        {
            pst=conn.prepareStatement(sql);
            pst.setString(1,jTextField1.getText());
            rs=pst.executeQuery();
            if(rs.next())
            {
                String add1=rs.getString("BookName");
                jTextField2.setText(add1);
                 String add2=rs.getString("Edition");
                jTextField3.setText(add2);
                 String add3=rs.getString("Publisher");
                jTextField4.setText(add3);
                 String add4=rs.getString("Price");
                jTextField5.setText(add4);
                 String add5=rs.getString("Pages");
                jTextField6.setText(add5);
                rs.close();
                pst.close();
                
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Book is not found");
            }
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }finally
        {
            try
            {
                pst.close();
                rs.close();
            }catch(Exception e)
            {
                
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

