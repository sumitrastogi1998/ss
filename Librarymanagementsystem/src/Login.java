public void Authentication()
    {
        if("".equals(jTextField1.getText()))
        {
            jTextField1.setBackground(Color.red);
        }else
        {
            jTextField1.setBackground(Color.white);
        }
        if("".equals(jPasswordField1.getText()))
        {
            jPasswordField1.setBackground(Color.red);
        }else
        {
            jPasswordField1.setBackground(Color.white);
        }
        
        String a1=jTextField1.getText();
        String a2=jPasswordField1.getText();
        String sql="Select * from NewAccount where Username='" +a1+ "' and Password='" +a2+"'";
    
    try
    {
        pst=conn.prepareStatement(sql);
        rs=pst.executeQuery();
        if(rs.next())
        {
            setVisible(false);
            Loading ob=new Loading();
            ob.setVisible(true);
            setVisible(false);
            Home ob1=new Home();
            ob1.setVisible(true);
        }else
        {
            JOptionPane.showMessageDialog(null,"Incorrect Username");
        }
    }catch(Exception e)
    {
        JOptionPane.showMessageDialog(null, e);
    }finally
    {
        try
        {
            pst.close();
            rs.close();
        }catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    
}

